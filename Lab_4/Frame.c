#include <stdio.h>

int main()
{
    int w, h;
    char ch;

    // Введення ширини
    printf("Введіть ширину: ");
    if (scanf("%d", &w) != 1)
    {
        printf("Помилка: некоректне введення числа.\n");
        return 1;
    }

    // Введення висоти
    printf("Введіть висоту: ");
    if (scanf("%d", &h) != 1)
    {
        printf("Помилка: некоректне введення числа.\n");
        return 1;
    }

    // Введення символу.
    // Пробіл перед %c у scanf(" %c", ...) важливий: він пропускає будь-які
    // пробільні символи (наприклад, натискання Enter після введення висоти),
    // що залишилися в буфері.
    printf("Введіть символ: ");
    if (scanf(" %c", &ch) != 1)
    {
        printf("Помилка: некоректне введення символу.\n");
        return 1;
    }

    // Перевірка умов: ширина та висота мають бути мінімум 2
    if (w < 2 || h < 2)
    {
        printf("Помилка: ширина та висота повинні бути не менше 2.\n");
        return 1;
    }

    // Зовнішній цикл відповідає за рядки (висота)
    for (int i = 0; i < h; i++)
    {
        // Внутрішній цикл відповідає за стовпці (ширина)
        for (int j = 0; j < w; j++)
        {
            // Логіка малювання:
            // Якщо це перший рядок (i == 0), останній рядок (i == h - 1),
            // перший стовпець (j == 0) або останній стовпець (j == w - 1) - друкуємо символ.
            if (i == 0 || i == h - 1 || j == 0 || j == w - 1)
            {
                printf("%c", ch);
            }
            else
            {
                // У всіх інших випадках (всередині рамки) друкуємо пробіл.
                printf(" ");
            }
        }
        // Перехід на новий рядок після завершення друку поточного рядка
        printf("\n");
    }

    return 0;
}
